     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->all();

        $notice = Notice::first();

        if ($notice) {
            $notice->update($data);
        } else {
            Notice::create($data);
        }
        Alert::toast("Notice Updated Successfully.", 'success');

        return redirect()->back();
    }

